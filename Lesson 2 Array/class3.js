// 4. Pust , Pop , Shift & Unshift 
let students = ["Tom","Jerry","MR Beem","Chotabeem"];

//Push
students.push("Mottu"); // Add item in array last
students.push("Pattlu");
console.log(students);

//Pop
students.pop(); // Array last item is removing (Pattlu is remove)
console.log(students);

//Shft
students.shift(); // Array first item is removing (Tom is remove)
console.log(students);

//Unshift
students.unshift(3); // Add item in array first
console.log(students);