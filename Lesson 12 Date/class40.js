// Some Date Converting Methods
let date = new Date();

console.log(date); // ISO formate date
console.log(date.toDateString()); // Normal formate date
console.log(date.toString()); // Normal formate date with time
console.log(date.toJSON()); // JSON formate
console.log(date.toLocaleDateString()); // Like 05/01/2021
console.log(date.toLocaleString()); // Like 05/01/2021, 14:36:31
console.log(date.toTimeString()); // Like 14:37:17 GMT+0530 (India Standard Time)
