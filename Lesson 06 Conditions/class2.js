// if and else
//if and else inside in a function
const calc = (a,b)=> {
    if (a <= b) {
        return a + b;
    }
    return a - b;
};
//if part
console.log("If Part: "+calc(5,5));
//else part
console.log("Else Part: "+calc(7,2));
